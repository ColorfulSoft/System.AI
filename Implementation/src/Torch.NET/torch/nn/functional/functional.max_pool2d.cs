                                                var oy = y + fy * dilation;
                                                if((oy >= 0) && (oy < inH) && (ox >= 0) && (ox < inW))
                                                {
                                                    var v = input.__data_float16[(((oy * inW) + ox) * inD + out_depth) * inB + batch];
                                                    if(v > max)
                                                    {
                                                        max = v;
                                                        max_x = ox;
                                                        max_y = oy;
                                                    }
                                                }
                                            }
                                        }
                                        Result.__data_float16[(((out_height * outW) + out_width) * outD + out_depth) * outB + batch] = max;
