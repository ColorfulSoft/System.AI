                            }
                        }
                        var src = data.__data_int8;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadSByte();
                        }
                        break;
                    }
                    case 5:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.int16);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.int16);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.int16);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.int16);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.int16);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.int16);
                                break;
                            }
                        }
                        var src = data.__data_int16;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadInt16();
                        }
                        break;
                    }
                    case 6:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.int32);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.int32);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.int32);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.int32);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.int32);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.int32);
                                break;
                            }
                        }
                        var src = data.__data_int32;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadInt32();
                        }
                        break;
                    }
                    case 7:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.int64);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.int64);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.int64);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.int64);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.int64);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.int64);
                                break;
                            }
                        }
                        var src = data.__data_int64;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadInt64();
                        }
                        break;
                    }
                    case 8:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.@bool);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.@bool);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.@bool);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.@bool);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.@bool);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.@bool);
                                break;
                            }
                        }
                        var src = data.__data_bool;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadBoolean();
                        }
                        break;
                    }
                }
                state_dict.Add(name, data);
            }
            return state_dict;
        }

