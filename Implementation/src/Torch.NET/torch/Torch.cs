                        {
                            src[i] = fs.ReadSingle();
                        }
                        break;
                    }
                    case 2:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.float64);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.float64);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.float64);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.float64);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.float64);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.float64);
                                break;
                            }
                        }
                        var src = data.__data_float64;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadDouble();
                        }
                        break;
                    }
                    case 3:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.uint8);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.uint8);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.uint8);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.uint8);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.uint8);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.uint8);
                                break;
                            }
                        }
                        var src = data.__data_uint8;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = fs.ReadByte();
                        }
                        break;
                    }
                    case 4:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.int8);
                                break;
                            }
                            case 1:
