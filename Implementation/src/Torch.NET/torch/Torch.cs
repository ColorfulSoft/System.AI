    ///<summary>The main class of Torch.NET.</summary>
    public static class Torch
    {

        ///<summary>Required to protect against parameter changes during calculations. If true, the settings are available for making changes.</summary>
        internal static bool __settings_are_available;

        ///<summary>Information about the current version and build of the library. Read-only.</summary>
        public static string library_info
        {

            get;

            private set;

        }

        ///<summary>A computational backend used for computations. Can be cpu or gpu.</summary>
        public static Backend backend
        {

            get;

            internal set;

        }

        ///<summary>Indicates whether the torch warning module is active.</summary>
        public static bool warnings_are_enabled
        {

            get;

            internal set;

        }

        ///<summary>Default data type.</summary>
        public static DType default_dtype
        {

            get;

            internal set;

        }

        ///<summary>Sets the default library settings.</summary>
        static Torch()
        {
            backend = Backend.cpu;
            default_dtype = DType.float32;
            warnings_are_enabled = true;
            library_info = "Torch.NET ver. 0.1 -> build 24.09.2019";
        }

        ///<summary>Specifies the required computational backend.</summary>
        ///<param name = "backend">Computational backend</param>
        public static void set_computation_backend(Backend backend)
        {
            if(__settings_are_available)
            {
                Torch.backend = backend;
            }
            else
            {
                __Warnings.warn("Changing settings is blocked. The calculation may already be running.", true);
            }
        }

        ///<summary>Specifies the required default data type.</summary>
        public static void set_default_type(DType dtype)
        {
            if(default_dtype == dtype)
            {
                __Warnings.warn("The current default data type is the same as the required one.");
            }
            else
            {
                default_dtype = dtype;
            }
        }

        ///<summary>Activates the torch warning module.</summary>
        public static void enable_warnings()
        {
            if(warnings_are_enabled)
            {
                __Warnings.warn("Torch warnings are already enabled.");
            }
            else
            {
                warnings_are_enabled = true;
            }
        }

        ///<summary>Deactivates the torch warning module.</summary>
        public static void disable_warnings()
        {
            if(!(warnings_are_enabled))
            {
                __Warnings.warn("TorchWarning: Torch warnings are already disabled.");
            }
            else
            {
                warnings_are_enabled = false;
            }
        }

        ///<summary>Saves state dict to a file.</summary>
        ///<param name = "state_dict">A dictionary containing a whole state of the module.</param>
        ///<param name = "fname">Output file name.</param>
        public static void save(Dictionary<string, Tensor> state_dict, string fname)
        {
            var fs = new BinaryWriter(File.Create(fname));
            foreach(var x in state_dict.Keys)
            {
                fs.Write(x);
                var data = state_dict[x];
                fs.Write(data.__ndim);
                fs.Write(data.__batch);
                fs.Write(data.__time);
                fs.Write(data.__depth);
                fs.Write(data.__height);
                fs.Write(data.__width);
                switch(data.dtype)
                {
                    case DType.float16:
                    case DType.half:
                    {
                        fs.Write((byte)0);
                        var src = data.__data_float16;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(Half.GetBits(src[i]));
                        }
                        break;
                    }
                    case DType.float32:
                    case DType.@float:
                    {
                        fs.Write((byte)1);
                        var src = data.__data_float32;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.float64:
                    case DType.@double:
                    {
                        fs.Write((byte)2);
                        var src = data.__data_float64;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.uint8:
                    {
                        fs.Write((byte)3);
                        var src = data.__data_uint8;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.int8:
                    {
                        fs.Write((byte)4);
                        var src = data.__data_int8;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.int16:
                    case DType.@short:
                    {
                        fs.Write((byte)5);
                        var src = data.__data_int16;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.int32:
                    case DType.@int:
                    {
                        fs.Write((byte)6);
                        var src = data.__data_int32;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.int64:
                    case DType.@long:
                    {
                        fs.Write((byte)7);
                        var src = data.__data_int64;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                    case DType.@bool:
                    {
                        fs.Write((byte)8);
                        var src = data.__data_bool;
                        for(int i = 0; i < src.Length; i++)
                        {
                            fs.Write(src[i]);
                        }
                        break;
                    }
                }
            }
            fs.Close();
        }

        ///<summary>Loads state dict from file.</summary>
        ///<param name = "fname">Input file name.</param>
        public static Dictionary<string, Tensor> load(string fname)
        {
            var fs = new BinaryReader(File.OpenRead(fname));
            var state_dict = new Dictionary<string, Tensor>();
            while(fs.BaseStream.Position + 1 < fs.BaseStream.Length)
            {
                var name = fs.ReadString();
                var ndim = fs.ReadByte();
                var batch = fs.ReadInt32();
                var time = fs.ReadInt32();
                var depth = fs.ReadInt32();
                var height = fs.ReadInt32();
                var width = fs.ReadInt32();
                var dtype = fs.ReadByte();
                Tensor data = null;
                switch(dtype)
                {
                    case 0:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.float16);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.float16);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.float16);
                                break;
                            }
                            case 3:
                            {
                                data = new Tensor(width, height, depth, dtype: DType.float16);
                                break;
                            }
                            case 4:
                            {
                                data = new Tensor(width, height, depth, time, dtype: DType.float16);
                                break;
                            }
                            case 5:
                            {
                                data = new Tensor(width, height, depth, time, batch, dtype: DType.float16);
                                break;
                            }
                        }
                        var src = data.__data_float16;
                        for(int i = 0; i < src.Length; i++)
                        {
                            src[i] = Half.ToHalf(fs.ReadUInt16());
                        }
                        break;
                    }
                    case 1:
                    {
                        switch(ndim)
                        {
                            case 0:
                            {
                                data = new Tensor(DType.float32);
                                break;
                            }
                            case 1:
                            {
                                data = new Tensor(width, dtype: DType.float32);
                                break;
                            }
                            case 2:
                            {
                                data = new Tensor(width, height, dtype: DType.float32);
                                break;
                            }
                            case 3:
