        ///<summary>Converts the current instance of the Size structure to a string representation.</summary>
        public override string ToString()
        {
            var str = "torch.Size([";
            switch(this.__ndim)
            {
                case 0:
                {
                    str += "])";
                    break;
                }
                case 1:
                {
                    str += this.__w.ToString() + "])";
                    break;
                }
                case 2:
                {
                    str += this.__h.ToString() + ", " + this.__w.ToString() + "])";
                    break;
                }
                case 3:
                {
                    str += this.__d.ToString() + ", " + this.__h.ToString() + ", " + this.__w.ToString() + "])";
                    break;
                }
                case 4:
                {
                    str += this.__t.ToString() + ", " + this.__d.ToString() + ", " + this.__h.ToString() + ", " + this.__w.ToString() + "])";
                    break;
                }
                case 5:
                {
                    str += this.__b.ToString() + ", " + this.__t.ToString() + ", " + this.__d.ToString() + ", " + this.__h.ToString() + ", " + this.__w.ToString() + "])";
                    break;
                }
                default:
                {
                    throw new TorchException("TorchException: Internal data error.");
                }
            }
            return str;
        }

