using System;

namespace torch
{

    ///<summary>Represents the size of the tensor.</summary>
    public struct Size
    {

        ///<summary>Number of measurements (0-5).</summary>
        internal byte __ndim;

        ///<summary>The width of the tensor.</summary>
        internal int __w;

        ///<summary>The height of the tensor.</summary>
        internal int __h;

        ///<summary>The depth of the tensor.</summary>
        internal int __d;

        ///<summary>The time dimension of the tensor.</summary>
        internal int __t;

        ///<summary>The batch size of the tensor.</summary>
        internal int __b;

        ///<summary>Initializes an instance of the Size structure.</summary>
        ///<param name = "length">The length of the tensor.</param>
        public Size(int length)
        {
            if(length < 1)
            {
                throw new TorchException("TorchException: The length of the tensor must be positive integer.");
            }
            this.__ndim = 1;
            this.__w = length;
            this.__h = 1;
            this.__d = 1;
            this.__t = 1;
            this.__b = 1;
        }

        ///<summary>Initializes an instance of the Size structure.</summary>
        ///<param name = "width">The width of the tensor.</param>
        ///<param name = "height">The height of the tensor.</param>
        public Size(int width, int height)
        {
            if(width < 1)
            {
                throw new TorchException("TorchException: The width of the tensor must be a positive integer.");
            }
            if(height < 1)
            {
                throw new TorchException("TorchException: The height of the tensor must be a positive integer.");
            }
            this.__ndim = 2;
            this.__w = width;
            this.__h = height;
            this.__d = 1;
            this.__t = 1;
            this.__b = 1;
        }

        ///<summary>Initializes an instance of the Size structure.</summary>
        ///<param name = "width">The width of the tensor.</param>
        ///<param name = "height">The height of the tensor.</param>
        ///<param name = "depth">The depth of the tensor.</param>
        public Size(int width, int height, int depth)
        {
            if(width < 1)
            {
                throw new TorchException("TorchException: The width of the tensor must be a positive integer.");
            }
            if(height < 1)
            {
                throw new TorchException("TorchException: The height of the tensor must be a positive integer.");
            }
            if(depth < 1)
            {
                throw new TorchException("TorchException: The depth of the tensor must be a positive integer.");
            }
            this.__ndim = 3;
            this.__w = width;
            this.__h = height;
            this.__d = depth;
            this.__t = 1;
            this.__b = 1;
        }

        ///<summary>Initializes an instance of the Size structure.</summary>
        ///<param name = "width">The width of the tensor.</param>
        ///<param name = "height">The height of the tensor.</param>
        ///<param name = "depth">The depth of the tensor.</param>
        ///<param name = "time">The time dimension of the tensor.</param>
        public Size(int width, int height, int depth, int time)
        {
            if(width < 1)
            {
                throw new TorchException("TorchException: The width of the tensor must be a positive integer.");
            }
            if(height < 1)
            {
                throw new TorchException("TorchException: The height of the tensor must be a positive integer.");
            }
            if(depth < 1)
            {
                throw new TorchException("TorchException: The depth of the tensor must be a positive integer.");
            }
            if(time < 1)
            {
                throw new TorchException("TorchException: The time dimension of the tensor must be a positive integer.");
            }
            this.__ndim = 4;
            this.__w = width;
            this.__h = height;
            this.__d = depth;
            this.__t = time;
            this.__b = 1;
        }

        ///<summary>Initializes an instance of the Size structure.</summary>
        ///<param name = "width">The width of the tensor.</param>
        ///<param name = "height">The height of the tensor.</param>
        ///<param name = "depth">The depth of the tensor.</param>
        ///<param name = "time">The time dimension of the tensor.</param>
        ///<param name = "batch">The batch size of the tensor.</param>
        public Size(int width, int height, int depth, int time, int batch)
        {
            if(width < 1)
            {
                throw new TorchException("TorchException: The width of the tensor must be a positive integer.");
            }
            if(height < 1)
            {
                throw new TorchException("TorchException: The height of the tensor must be a positive integer.");
            }
            if(depth < 1)
            {
                throw new TorchException("TorchException: The depth of the tensor must be a positive integer.");
            }
            if(time < 1)
            {
                throw new TorchException("TorchException: The time dimension of the tensor must be a positive integer.");
            }
            if(batch < 1)
            {
                throw new TorchException("TorchException: The batch dimension of the tensor must be a positive integer.");
            }
            this.__ndim = 5;
            this.__w = width;
            this.__h = height;
            this.__d = depth;
            this.__t = time;
            this.__b = batch;
        }

        ///<summary>Returns a dimension.</summary>
        public int this[int i]
        {

            get
            {
                if(i > (this.__ndim - 1))
                {
                    throw new TorchException("TorchException: It is impossible to obtain a " + i.ToString() + " measurement of the " + this.__ndim.ToString() + " - dimensional tensor.");
                }
                switch(i)
                {
                    case 0:
                    {
                        return this.__w;
                    }
                    case 1:
                    {
                        return this.__h;
                    }
                    case 2:
                    {
                        return this.__d;
                    }
                    case 3:
                    {
                        return this.__t;
                    }
                    case 4:
                    {
                        return this.__b;
                    }
                    default:
                    {
                        return 0;
                    }
                }
            }

        }

        public static implicit operator Size(byte[] size)
        {
            if(size == null)
            {
                throw new TorchException("TorchException: Cannot convert an empty object.");
            }
            switch(size.Length)
            {
                case 0:
                {
                    return new Size();
                }
                case 1:
                {
                    return new Size((int)size[0]);
                }
                case 2:
                {
                    return new Size((int)size[0], (int)size[1]);
                }
                case 3:
                {
                    return new Size((int)size[0], (int)size[1], (int)size[2]);
                }
                case 4:
                {
                    return new Size((int)size[0], (int)size[1], (int)size[2], (int)size[3]);
                }
                case 5:
                {
                    return new Size((int)size[0], (int)size[1], (int)size[2], (int)size[3], (int)size[4]);
                }
                default:
                {
                    throw new TorchException("TorchException: Invalid view of size. Arrays with more than 5 dimensions are not supported.");
                }
            }
        }

        public static implicit operator Size(byte size)
        {
            return new Size((int)size);
        }

