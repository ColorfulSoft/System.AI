        public static implicit operator Size(long size)
        {
            return new Size((int)size);
        }

        public static implicit operator byte[](Size size)
        {
            var result = new byte[size.__ndim];
            switch(size.__ndim)
            {
                case 0:
                {
                    return result;
                }
                case 1:
                {
                    result[0] = (byte)size.__w;
                    return result;
                }
                case 2:
                {
                    result[0] = (byte)size.__w;
                    result[1] = (byte)size.__h;
                    return result;
                }
                case 3:
                {
                    result[0] = (byte)size.__w;
                    result[1] = (byte)size.__h;
                    result[2] = (byte)size.__d;
                    return result;
                }
                case 4:
                {
                    result[0] = (byte)size.__w;
                    result[1] = (byte)size.__h;
                    result[2] = (byte)size.__d;
                    result[3] = (byte)size.__t;
                    return result;
                }
                case 5:
                {
                    result[0] = (byte)size.__w;
                    result[1] = (byte)size.__h;
                    result[2] = (byte)size.__d;
                    result[3] = (byte)size.__t;
                    result[4] = (byte)size.__b;
                    return result;
                }
                default:
                {
                    throw new TorchException("TorchException: The original instance of the size structure is corrupted.");
                }
            }
        }

